package domain.enums;

/**
 * ENUM: AccountType
 * 
 * A catalog of the different kinds of bank accounts we can have.
 */
public enum AccountType {
    SAVINGS,     // An account where you save money and earn a little interest
    CHECKING,    // An account you use for daily expenses, often with a checkbook
    PERSONAL,    // A general account for a person
    CORPORATE    // An account for a business
}